using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace class_x_1
{
    class Ball
    {
        int size;
        string color;
        string type;
        string fabric;

        public int get_size()
        {
            return this.size;
        }
        public void set_size(int size)
        {
            this.size = size;
        }
        public string get_color()
        {
            return this.color;
        }
        public void set_color(string color)
        {
            this.color = color;
        }
        public string get_type()
        {
            return this.type;
        }
        public void set_type(string type)
        {
            this.type = type;
        }
        public string get_fabric()
        {
            return this.fabric;
        }
        public void set_fabric(string fabric)
        {
            this.fabric = fabric;
        }

        public Ball(int size, string color, string type, string fabric)
        {
            this.size = size;
            this.color = color;
            this.type = type;
            this.fabric = fabric;
        }

    }

}
